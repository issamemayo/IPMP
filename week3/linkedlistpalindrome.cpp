class Solution{
  public:
    //Function to check whether the list is palindrome.
    bool isPalindrome(Node *head)
    {
        stack<Node*> s;
        Node*travel=head;
        while(travel!=nullptr){
            s.push(travel);
            travel=travel->next;
        }
        travel=head;
        for(int i=0;i<s.size();i++){
            Node*tmp= s.top();
            if(tmp->data!=travel->data){
                return false;
            }
            travel=travel->next;
            s.pop();
            
        }
        return true;
    }
};



